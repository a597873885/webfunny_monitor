module.exports = {
  // ============================================================
  // 源服务器配置
  // ============================================================
  source: {
    // 服务器地址（包含协议和端口）
    host: 'http://源服务器IP:8123',
    
    // 用户名
    username: '用户名',
    
    // 密码
    password: '密码',
    
    // 源数据库名称
    database: '源数据库名'
  },

  // ============================================================
  // 目标服务器配置
  // ============================================================
  target: {
    // 服务器地址（包含协议和端口）
    // 注意：如果是同服务器迁移，这个地址会被忽略，使用 source.host
    host: 'http://目标服务器IP:8123',
    
    // 用户名
    username: '用户名',
    
    // 密码
    password: '密码',
    
    // 目标数据库名称
    database: '目标数据库名'
  },

  // ============================================================
  // 迁移选项
  // ============================================================
  options: {
    // 每批迁移的行数
    // 建议值：
    //   - 小表（< 100万行）：10000 - 50000
    //   - 大表（> 100万行）：5000 - 10000
    //   - 网络较慢时：减小批次大小
    batchSize: 10000,
    
    // 是否启用调试模式（显示详细的SQL语句）
    debug: false,
    
    // 超时时间（毫秒）
    timeout: 300000,  // 5分钟
    
    // 是否为集群模式（控制表引擎转换）
    // 
    // true: 将 MergeTree 转换为 ReplicatedMergeTree（集群复制模式）
    //       适用于：多节点集群、需要数据复制的场景
    //       要求：目标服务器已配置 ZooKeeper
    // 
    // false: 保持普通 MergeTree 引擎（单节点模式）
    //        适用于：单节点部署、测试环境
    // 
    // undefined（或不设置此项）: 自动检测
    //           从 ../config/local_db.js 加载集群配置
    //           如果检测到集群配置则为 true，否则为 false
    // 
    isCluster: true
  }
}

